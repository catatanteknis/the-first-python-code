# perulangan FOR
j_buku = 10
j_baca = 0

for j_baca in range(1, j_buku+1):
    print(f"Ibu saya sudah selesai membaca {j_baca} buku")

# perulangan while
s_baca = 0

while s_baca < j_buku:
    s_baca += 1
    print(f"Sekarang sudah membaca {s_baca} buku")
else:
    print(f"Sudah membaca {s_baca} buku")
